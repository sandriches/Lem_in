#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t c;

	c = 0;
	while (s1[c] == s2[c] && s1[c] != '\0' && s2[c] != '\0')
		c++;
	if (s1[c] == '\0' && s2[c] == '\0')
		return (1);
	return (0);
}
